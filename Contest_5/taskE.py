def compute_prefix(p):
    """
    Вычисляет префикс-функцию для строки p.
    Префикс-функция - это массив, где prefix[i] - длина наибольшего собственного суффикса 
    подстроки p[0..i], совпадающего с её префиксом.
    """
    m = len(p)
    prefix = [0] * m  # Инициализируем массив префикс-функции нулями
    j = 0  # Указатель на текущий символ префикса
    
    # Начинаем с 1, так как prefix[0] всегда равен 0
    for i in range(1, m):
        # Пока есть несовпадение и j > 0, возвращаем j назад
        while j > 0 and p[i] != p[j]:
            j = prefix[j - 1]
        
        # Если символы совпали, увеличиваем j
        if p[i] == p[j]:
            j += 1
            prefix[i] = j  # Записываем текущую длину совпадения
    
    return prefix


def find_matches_with_mismatch(p, t):
    """
    Находит все вхождения строки p в строку t с допущением не более 1 несовпадения.
    Возвращает список позиций (1-индексированных), где начинаются такие вхождения.
    """
    m = len(p)
    n = len(t)
    
    # Если p длиннее t, вхождений быть не может
    if m > n:
        return []

    # Вычисляем префикс-функцию для p (хотя в текущей реализации она не используется)
    prefix = compute_prefix(p)
    matches = []  # Список для хранения найденных позиций

    # Перебираем все возможные начальные позиции в t
    for start in range(n - m + 1):
        mismatches = 0  # Счетчик несовпадений
        j = 0  # Указатель на текущий символ в p
        
        # Сравниваем подстроку t[start..start+m-1] с p
        for i in range(start, start + m):
            if t[i] != p[j]:
                mismatches += 1
                # Если несовпадений больше 1, прерываем проверку
                if mismatches > 1:
                    break
            j += 1
        
        # Если несовпадений 1 или меньше, добавляем позицию
        if mismatches <= 1:
            matches.append(start + 1)  # +1 для перехода к 1-индексации

    return matches


def main():
    """
    Основная функция: читает входные данные, ищет совпадения и выводит результат.
    """
    import sys
    # Читаем строки p (шаблон) и t (текст) из стандартного ввода
    p = sys.stdin.readline().strip()
    t = sys.stdin.readline().strip()

    # Находим все вхождения p в t с одним несовпадением
    matches = find_matches_with_mismatch(p, t)
    
    # Выводим количество найденных вхождений
    print(len(matches))
    
    # Если есть совпадения, выводим их позиции через пробел
    if matches:
        print(' '.join(map(str, matches)))


if __name__ == "__main__":
    """
    Точка входа в программу. Вызывается только при прямом запуске скрипта.
    """
    main()