n = int(input())  # длина стопки контейнеров

# dp[i][0] - количество безопасных стопок длины i, заканчивающихся на B или C
# dp[i][1] - количество безопасных стопок длины i, заканчивающихся на A
dp = [[0, 0] for _ in range(n+1)]

# Базовые случаи:
dp[0] = [0, 0]  # нет стопки
dp[1] = [2, 1]   # стопки длины 1: B, C (2 варианта) и A (1 вариант)

for i in range(2, n+1):
    # Стопка, заканчивающаяся на B/C:
    # предыдущая может заканчиваться на A, B или C (все варианты)
    B_C = (dp[i-1][0] + dp[i-1][1]) * 2  # умножаем на 2 (B или C)
    
    # Стопка, заканчивающаяся на A:
    # предыдущая должна заканчиваться на B или C
    A = dp[i-1][0]  # только 1 способ (добавить A)
    
    dp[i] = [B_C, A]  # обновляем значения

# Общее количество безопасных стопок длины n
print(dp[n][0] + dp[n][1])